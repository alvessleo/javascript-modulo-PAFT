// 2. Em seguida, programe os métodos para calcular a área 
// (PI*raio*raio) e o perímetro do círculo (2*PI*raio)

function area(raio) {
    let formula_area = Math.PI*raio*raio
    return formula_area
}
  
function perimetro(raio) {
    let formula_perimetro = 2*Math.PI*raio
    return formula_perimetro
}